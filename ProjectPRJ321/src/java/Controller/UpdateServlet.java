    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        int id= Integer.parseInt(request.getParameter("id"));
        String name=request.getParameter("name");
        String image=request.getParameter("image");
        String describe=request.getParameter("describe");
         int rest = Integer.parseInt(request.getParameter("rest"));
        float price = Float.parseFloat(request.getParameter("price"));
        float pricebefore = Float.parseFloat(request.getParameter("priceafter"));
        ProductDAO cdb=new ProductDAO();
        Product c=new Product(id, name, image, price,pricebefore,describe,rest);
        cdb.update(c);
        response.sendRedirect("admin/AdminPage.jsp");
    }

